package com.Raunak;

//Wap to find first 10 natural nos using while loop;
public class Natural10Number {
    public static void main(String[] args) {
        int num=10;
        System.out.println("the first 10 natural number is  :");
        for(int i=1;i<=num;i++)
        {
            System.out.println(i);
        }
    }
}
